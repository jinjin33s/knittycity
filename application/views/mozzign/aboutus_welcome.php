        <p>Much of my life has revolved around working with my hands and enjoying the benefits of a community. Before opening Knitty City, I had a wholesale business called ‘A Thousand Cranes”. I had the pleasure of working with beautiful Japanese washi paper, folding origami, and using kimono textiles to create scarves and quilts. In 2001, when I hooked into the knitting world via the Internet, I was amazed at its existence and the size of it. </p>
        <p>During my childhood, my parents owned a grocery store: Bob’s Supermarket. It was a beloved store and we knew everyone. “Bob’s” was the neighborhood hub. So, when I envisioned Knitty City, I knew what I wanted: A physical place where the whole knitting community could come together to celebrate working with their hands, explore beautiful materials and colors, and learn from one another, Besides being a hangout for knitters, I wanted it to be a place where babies, kids, brothers, sisters, husbands, boyfriends, grandparents could come and feel comfortable.</p>
        <p>Our store is not only about the yarns. It’s about living a joyful life and sharing our stories.  My daughter, Julie, came up with the name “Knitty City”. Perfect!</p>
