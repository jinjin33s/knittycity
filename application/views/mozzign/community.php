            <p>
               Following  is an excerpt from the NYAWC newsletter that illustrates the healing and progress that staff members have reported:
            </p>
            <p>
            <span class="orangeitalgeorgia">knitting project helps women heal</span><br>
                For the last three months, NYAWC has held knitting projects for the women staying at its shelters. Every Friday night, at two shelters—Rose House and Peace House—women come together for a late-night knitting session. The women enjoy the activity so much that very often the knitting session, planned to meet for one hour, lasts for up to three hours. The knitting sessions provide opportunities for the battered women to de-stress and socialize with their housemates.
            </p>

        </div>
        <div class="box" style="margin-right:0px;">
            <p>
                Sometimes they bring their kids. At other times, they bring food to share with each other. NYAWC staff members Hifsa Rajput and Uzma Naz organize the knitting sessions, as well as provide assistance. Yarn and knitting supplies are generously donated by Knitty City.
            </p>
