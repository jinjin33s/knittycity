        Knitty City is a community so we thought it made sense to help others form their own communities.  Here’s how we do that:    </div>
    <div class="box" style="width: 244px;">
        <div class="orangeitalgeorgia" style="text-align: right;color: #b3b3b3; margin-top:40px;">
        There’s a Chinese proverb states that if you give a man a fish, he would eat for a day, but, if you teach him how to fish, he will eat for life.
        </div>
        <div class="box" style="float: right; margin-right: 0px;">
            <p>So, instead of giving away finished products to charities, we thought we would supply non-profit groups with yarn & needles. We have seen many friendships develop among our customers, and, with that in mind, we sought to develop “Knitting Circles” within non-profits.
            </p>
            <p>For our first project at the New York Asian Women’s Center, we provided yarn & needles for 40 women in  two “safe houses” where they are protected from violence and abuse.  Manufacturers such as Tahki, Stacy Charles and Cascade  Yarn donated yarns. Eileen Fisher donated fabrics for sewing & quilting projects, and our customers gave us their extra “stash” materials.
            </p>
        </div>
    </div>
