        <p>Opened in January of 2006, Knitty City is the realized dream of Pearl Chin, who designed it as a “ Yarn Studio”. It is devoted to beautiful materials for knitting and crochet , instructional classes and community connection.</p>

        <p>Knitty City’s selection of yarns reflects some of the best brands in the business. New and interesting yarns, and the products that complement them, are always popping up at Knitty City. In addition, the store has one of the most comprehensive and up-to-date collections of knitting, crochet and needle art books, magazines and patterns in New York.</P>

        <p>Knitty City is committed to the people behind the yarn and designs, with a shared passion for the artistry that goes into their products. Book signings and events that allow friends and customers to meet authors and designers are regularly scheduled, and new and emerging young talent is highlighted with a monthly “Young Designer” Series.</p>
